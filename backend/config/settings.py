# Database
# https://docs.djangoproject.com/en/6.0/ref/settings/#databases

# Support DATABASE via environment variable `DBURL` (or `DATABASE_URL`).
# Optionally use `dj-database-url` if installed to parse the URL.
DATABASE_URL = get_env("database_url", "DATABASE_URL", "DBURL")
DIRECT_URL = get_env("direct_url", "DIRECT_URL", "DIRECTURL")

if DATABASE_URL and dj_database_url:
    DATABASES = {
        'default': dj_database_url.parse(DATABASE_URL, conn_max_age=600)
    }
elif DATABASE_URL and not dj_database_url:
    # Minimal parsing for postgres URL if dj_database_url is not available.
    # Expect format: postgres://USER:PASS@HOST:PORT/NAME
    import urllib.parse as urlparse

    parsed = urlparse.urlparse(DATABASE_URL)
    DATABASES = {
        'default': {
            'ENGINE': 'django.db.backends.postgresql',
            'NAME': parsed.path.lstrip('/'),
            'USER': parsed.username,
            'PASSWORD': parsed.password,
            'HOST': parsed.hostname,
            'PORT': parsed.port or '',
        }
    }
else:
    DATABASES = {
        'default': {
            'ENGINE': 'django.db.backends.sqlite3',
            'NAME': BASE_DIR / 'db.sqlite3',
        }
    }

